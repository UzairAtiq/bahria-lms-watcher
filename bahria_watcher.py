def _extract_course_name(url: str, text: str) -> str:
    """
    Best-effort extraction of a course name from the URL path or link text.
    Falls back to 'General' if nothing useful is found.
    """
    # Try common patterns like /CS101/ or /course/CS-101/
    patterns = [
        r"/([A-Z]{2,6}[-\s]?\d{3,4}[A-Z]?)/",   # e.g. CS101, EE-302
        r"/courses?/([^/]+)/",
        r"/subject/([^/]+)/",
    ]
    for pat in patterns:
        m = re.search(pat, url, re.IGNORECASE)
        if m:
            return m.group(1).strip()

    # Try to find course-like tokens in link text
    m = re.search(r"\b([A-Z]{2,6}[-\s]?\d{3,4})\b", text)
    if m:
        return m.group(1)

    return "General"


def _filename_from_download_url(url: str, label: str, row_text: str) -> str:
    """
    Extract a meaningful filename from a Download.php?k=<base64> URL.

    The LMS encodes metadata in the 'k' parameter. The base64 value typically
    decodes to a comma-separated string whose last CSV-like field is the
    original filename (e.g. "CCP--AI-07042026-031901pm.pdf").

    Falls back to label text → row text extract → timestamp.
    """
    import base64
    from urllib.parse import urlparse, parse_qs

    try:
        parsed   = urlparse(url)
        qs       = parse_qs(parsed.query)
        k_values = qs.get("k", [])
        if k_values:
            # Add padding just in case
            k_b64   = k_values[0] + "=="
            decoded = base64.b64decode(k_b64).decode("utf-8", errors="ignore")
            # Decoded string often: "id,date,type,flag,filename,domain,..."
            parts = decoded.split(",")
            for part in reversed(parts):
                part = part.strip()
                # Look for a field that ends with a known file extension
                if re.search(r"\.(pdf|docx?|pptx?|xlsx?|zip|rar|7z|txt)$",
                             part, re.IGNORECASE):
                    return sanitize_filename(part)
    except Exception:
        pass

    # Fallback 1: use the link label text
    if label:
        clean = sanitize_filename(label)
        if len(clean) > 3:
            return clean + ".pdf"   # assume PDF if unknown

    # Fallback 2: first file-like token in the row text
    m = re.search(r"[\w\-]+\.(pdf|docx?|pptx?|xlsx?|zip|rar)", row_text,
                  re.IGNORECASE)
    if m:
        return sanitize_filename(m.group(0))

    # Fallback 3: timestamp-based name
    return f"assignment_{datetime.utcnow().strftime('%Y%m%d_%H%M%S')}.pdf"



