def sanitize_filename(name: str) -> str:
    """Remove characters that are invalid in file/directory names."""
    return re.sub(r'[\\/*?:"<>|]', "_", name).strip()


def download_file(url: str, dest_path: Path, cookies: dict) -> bool:
    """Download a single file using httpx and the current session cookies."""
    try:
        dest_path.parent.mkdir(parents=True, exist_ok=True)
        with httpx.stream("GET", url, cookies=cookies, follow_redirects=True,
                          timeout=60) as resp:
            resp.raise_for_status()
            with open(dest_path, "wb") as f:
                for chunk in resp.iter_bytes(chunk_size=8192):
                    f.write(chunk)
        log.info(f"Downloaded → {dest_path}")
        return True
    except Exception as e:
        log.error(f"Download failed for {url}: {e}")
        return False


